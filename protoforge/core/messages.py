def msg(protocol: str, key: str, **kwargs) -> str:
    template = PROTOCOL_MESSAGES.get(protocol, {}).get(key, key)
    if kwargs:
        try:
            return template.format(**kwargs)
        except KeyError:
            return template
    return template


def desc(key: str, lang: str = "", default: str = "") -> str:
    """Get a localized description by key.

    Args:
        key: Message key (e.g. "protocol.modbus_tcp", "protocol.modbus_tcp.desc")
        lang: Language code ("zh" or "en"). Defaults to "zh".
        default: Default value if key not found. Defaults to "".
    """
    if not lang:
        lang = "zh"
    # First try TEST_MESSAGES (protocol + testing i18n)
    msg = TEST_MESSAGES.get(lang, {}).get(key)
    if msg:
        return msg
    # Fallback to CONFIG_DESCRIPTIONS (legacy)
    if default:
        return CONFIG_DESCRIPTIONS.get(key, default)
    return CONFIG_DESCRIPTIONS.get(key, key)


