# Default language
_DEFAULT_LANG = "zh"


def tmsg(key: str, lang: str = "", **kwargs) -> str:
    """Get a testing i18n message by key and language.

    Args:
        key: Message key (e.g. "suite_quick_test", "case_device_test")
        lang: Language code ("zh" or "en"). Defaults to _DEFAULT_LANG.
        **kwargs: Format parameters for the message template.
    """
    if not lang or lang not in TEST_MESSAGES:
        lang = _DEFAULT_LANG
    template = TEST_MESSAGES.get(lang, {}).get(key, key)
    if kwargs:
        try:
            return template.format(**kwargs)
        except KeyError:
            return template
    return template


def get_lang_from_request(request) -> str:
    """Extract language preference from HTTP request Accept-Language header.

    Returns "zh" for Chinese (default), "en" for English.
    """
    accept_lang = request.headers.get("accept-language", "")
    if "zh" in accept_lang.lower():
        return "zh"
    if "en" in accept_lang.lower():
        return "en"
    return _DEFAULT_LANG
